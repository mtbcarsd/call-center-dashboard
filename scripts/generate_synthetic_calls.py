async def _synth_turn(text: str, voice: str, path: str) -> None:
    communicate = edge_tts.Communicate(text, voice)
    await communicate.save(path)


async def _build_call(call_topic: str, turns: list[tuple[str, str]]) -> None:
    tmp_dir = os.path.join(TEST_DATA_DIR, f".tmp_{call_topic}")
    os.makedirs(tmp_dir, exist_ok=True)

    audio = AudioSegment.silent(duration=0)
    pause = AudioSegment.silent(duration=PAUSE_MS)
    transcript_words = []

    for i, (speaker, text) in enumerate(turns):
        voice = OPERATOR_VOICE if speaker == "operator" else CLIENT_VOICE
        turn_path = os.path.join(tmp_dir, f"{i:02d}_{speaker}.mp3")
        await _synth_turn(text, voice, turn_path)
        audio += AudioSegment.from_file(turn_path) + pause
        transcript_words.append(text)

    wav_path = os.path.join(TEST_DATA_DIR, f"{call_topic}.wav")
    audio.export(wav_path, format="wav")

    txt_path = os.path.join(TEST_DATA_DIR, f"{call_topic}.txt")
    with open(txt_path, "w", encoding="utf-8") as f:
        f.write(" ".join(transcript_words))

    for f in os.listdir(tmp_dir):
        os.remove(os.path.join(tmp_dir, f))
    os.rmdir(tmp_dir)

    print(f"  {call_topic}: {audio.duration_seconds:.1f}с → {wav_path}")


async def main() -> None:
    os.makedirs(TEST_DATA_DIR, exist_ok=True)
    print(f"Генерирую {len(SCENARIOS)} синтетических звонков в {TEST_DATA_DIR}...")
    for call_topic, turns in SCENARIOS.items():
        await _build_call(call_topic, turns)
    print("Готово.")


if __name__ == "__main__":
    asyncio.run(main())
